    window.scrollTo({ top: y, behavior: 'smooth' });
    
    document.querySelectorAll('.faq-nav .custom-link').forEach(link => {
      link.classList.remove('text-main', 'fw-medium');
      link.classList.add('text-secondary', 'fw-light');
    });
    
    const activeLink = document.querySelector(`.faq-nav a[href="#${sectionId}"]`);
    if (activeLink) {
       activeLink.classList.remove('text-secondary', 'fw-light');
       activeLink.classList.add('text-main', 'fw-medium');
    }
  }
};
